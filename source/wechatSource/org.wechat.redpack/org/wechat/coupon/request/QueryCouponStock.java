package org.wechat.coupon.request;

import org.wechat.pay.annotation.NotSign;

/**
 * 查询代金卷批次请求对象
 * 
 * @author Andy
 * 
 */
public class QueryCouponStock implements java.io.Serializable {
	/**
	 * 
	 */
	@NotSign
	private static final long serialVersionUID = 1L;
	private String coupon_stock_id; // 代金卷批次id
	private String appid; // 公众账号ID
	private String mch_id; // 商户号
	private String op_user_id; // 操作员 否
	private String device_info; // 设备号 否
	private String nonce_str; // 随即字符串
	private String sign; // 签名
	private String version; // 协议版本 否
	private String type = "XML"; // 默认为xml 否

	public String getCoupon_stock_id() {
		return coupon_stock_id;
	}

	public void setCoupon_stock_id(String coupon_stock_id) {
		this.coupon_stock_id = coupon_stock_id;
	}

	public String getAppid() {
		return appid;
	}

	public void setAppid(String appid) {
		this.appid = appid;
	}

	public String getMch_id() {
		return mch_id;
	}

	public void setMch_id(String mch_id) {
		this.mch_id = mch_id;
	}

	public String getOp_user_id() {
		return op_user_id;
	}

	public void setOp_user_id(String op_user_id) {
		this.op_user_id = op_user_id;
	}

	public String getDevice_info() {
		return device_info;
	}

	public void setDevice_info(String device_info) {
		this.device_info = device_info;
	}

	public String getNonce_str() {
		return nonce_str;
	}

	public void setNonce_str(String nonce_str) {
		this.nonce_str = nonce_str;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
